    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        try{
           if(idAlteracao==0){
            String[] hobbies = txtHobbies.getText().split(",");
            Connection conexao = DriverManager.getConnection("jdbc:mysql://localhost:3306/persona","root","");
            String sqlPersona = "INSERT INTO persona(nome,email,telefone,genero,profissao,salario,bairro,cidade,estado,hobbies)VALUES(?,?,?,?,?,?,?,?,?,?)";
            PreparedStatement stmt = conexao.prepareStatement(sqlPersona);
            stmt.setString(1,this.txtNome.getText());
            stmt.setString(2,this.txtEmail.getText());
            stmt.setInt(3,Integer.parseInt(this.txtTelefone.getText()));             
            stmt.setString(4,this.txtGenero.getText());
            stmt.setString(5,this.txtProfissao.getText());
            stmt.setDouble(6,Double.parseDouble(this.txtSalario.getText()));
            stmt.setString(7,this.txtBairro.getText());
            stmt.setString(8,this.txtCidade.getText());
            stmt.setString(9,this.txtEstado.getText());
            stmt.setString(10,Arrays.toString(hobbies));
            stmt.execute();
            stmt.close();
            conexao.close();
            JOptionPane.showMessageDialog(this, "Persona Cadastrada!");
        }else{
            String[] hobbies = txtHobbies.getText().split(",");           
            Connection conexao = DriverManager.getConnection("jdbc:mysql://localhost:3306/persona","root","");    
            String editarPersonaSQL = "UPDATE persona SET nome = ?, email = ?,telefone = ?,genero = ?, profissao = ?,salario = ?,bairro = ?,"
                    + "cidade = ?,estado = ?,hobbies = ? WHERE  id = ?" ;           
            PreparedStatement stmt = conexao.prepareStatement(editarPersonaSQL);           
            stmt.setString(1,this.txtNome.getText());
            stmt.setString(2,this.txtEmail.getText());
            stmt.setInt(3,Integer.parseInt(this.txtTelefone.getText()));            
            stmt.setString(4,this.txtGenero.getText());
            stmt.setString(5,this.txtProfissao.getText());
            stmt.setDouble(6,Double.parseDouble(this.txtSalario.getText()));
            stmt.setString(7,this.txtBairro.getText());
            stmt.setString(8,this.txtCidade.getText());
            stmt.setString(9,this.txtEstado.getText());            
            stmt.setString(10,Arrays.toString(hobbies));
            stmt.setInt(11,idAlteracao);
            stmt.execute();
            stmt.close();
            conexao.close();
            JOptionPane.showMessageDialog(this, "Persona Atualizada!"); 
            }
           this.dispose();
        }catch(SQLException excecao) {
            JOptionPane.showMessageDialog(this,excecao);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

