    public CadastrarPersona(int idAlteracao){ //aqui pega os valores do banco e exibe dentro da janela preenchida
        initComponents();
        this.idAlteracao = idAlteracao; 
        
        try{
            
            Connection conexao = DriverManager.getConnection("jdbc:mysql://localhost:3306/persona","root","");
            String editarPersonaSQL = "SELECT * FROM Persona WHERE Id = ?";
            PreparedStatement stmt = conexao.prepareStatement(editarPersonaSQL);
            stmt.setInt(1, idAlteracao);
            ResultSet dadosSalvosPersonaSQL = stmt.executeQuery();            
            while(dadosSalvosPersonaSQL.next()){
                txtID.setText(dadosSalvosPersonaSQL.getString("Id"));
                txtID.setEnabled(false); //não permite a edição do campo
                txtNome.setText(dadosSalvosPersonaSQL.getString("Nome"));
                txtEmail.setText(dadosSalvosPersonaSQL.getString("Email"));
                txtTelefone.setText(dadosSalvosPersonaSQL.getString("Telefone"));                 
                txtGenero.setText(dadosSalvosPersonaSQL.getString("Genero"));
                txtProfissao.setText(dadosSalvosPersonaSQL.getString("Profissao"));
                txtSalario.setText(dadosSalvosPersonaSQL.getString("Salario"));
                txtBairro.setText(dadosSalvosPersonaSQL.getString("Bairro"));
                txtCidade.setText(dadosSalvosPersonaSQL.getString("Cidade"));
                txtEstado.setText(dadosSalvosPersonaSQL.getString("Estado"));
                txtHobbies.setText(dadosSalvosPersonaSQL.getString("Hobbies"));
            }
            stmt.close();
            conexao.close();
        }catch(SQLException excecao){
            JOptionPane.showMessageDialog(this,excecao);
        }
    }

